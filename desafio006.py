# Crie um algoritmo que leia
# um número e mostre o seu dobro, triplo e raiz quadrada.
numero = int( input('Informe um número inteiro: ') )
print('Número:', numero)
print('Dobro de {0} = {1}'.format(numero, numero * 2) )
print('Tripo de {0} = {1}'.format(numero, numero * 3) )
print('Raiz quadrada de {0} = {1}'.format(numero, numero ** 0.5) )