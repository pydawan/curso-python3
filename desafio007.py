#!/usr/bin/env python3

# Desafio 007
# Desenvolva um programa que leia as duas notas
# de um aluno, calcule e mostre sua média.
nota1 = int( input('Digite a nota 1: ') )
nota2 = int( input('Digite a nota 2: ') )
media = (nota1 + nota2) / 2
print('Nota 1:', nota1)
print('Nota 2:', nota2)
print('Média:', media)
